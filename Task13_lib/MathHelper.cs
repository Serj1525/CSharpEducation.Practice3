namespace Task13_lib;

public class MathHelper
{
        public int Addiction(int a, int b)
        {
            return a + b;
        }
        public int Subtraction(int a, int b)
        {
            return a - b;
        }
        public int Multiplication(int a, int b)
        {
            return a * b;
        }
        public double Division(double a, double b)
        {
            return a/b;
        }
}